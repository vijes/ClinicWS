-- Database Schema for Clinica Management System
CREATE SCHEMA IF NOT EXISTS SCCLINIC;

-- 1. PERSONAS
CREATE TABLE SCCLINIC.PERSONAS (
    ID UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    NOMBRES VARCHAR(100) NOT NULL,
    APELLIDOS VARCHAR(100) NOT NULL,
    DOCUMENTO VARCHAR(20) NOT NULL UNIQUE,
    FECHA_NACIMIENTO DATE NOT NULL,
    SEXO VARCHAR(20),
    DIRECCION VARCHAR(255),
    TELEFONO VARCHAR(20),
    EMAIL VARCHAR(100),
    -- Audit fields
    USER_CREATE VARCHAR(100) NOT NULL,
    DATE_CREATE TIMESTAMP WITHOUT TIME ZONE DEFAULT CURRENT_TIMESTAMP NOT NULL,
    USER_MODIFICATION VARCHAR(100),
    DATE_MODIFICATION TIMESTAMP WITHOUT TIME ZONE,
    COMPANY_CODE VARCHAR(20) NOT NULL
);

COMMENT ON TABLE SCCLINIC.PERSONAS IS 'Almacena la información básica de todas las personas en el sistema';
COMMENT ON COLUMN SCCLINIC.PERSONAS.ID IS 'Identificador único de la persona';
COMMENT ON COLUMN SCCLINIC.PERSONAS.NOMBRES IS 'Nombres de la persona';
COMMENT ON COLUMN SCCLINIC.PERSONAS.APELLIDOS IS 'Apellidos de la persona';
COMMENT ON COLUMN SCCLINIC.PERSONAS.DOCUMENTO IS 'Documento de identidad único';
COMMENT ON COLUMN SCCLINIC.PERSONAS.FECHA_NACIMIENTO IS 'Fecha de nacimiento';
COMMENT ON COLUMN SCCLINIC.PERSONAS.SEXO IS 'Sexo o género';
COMMENT ON COLUMN SCCLINIC.PERSONAS.DIRECCION IS 'Dirección de residencia';
COMMENT ON COLUMN SCCLINIC.PERSONAS.TELEFONO IS 'Número de teléfono';
COMMENT ON COLUMN SCCLINIC.PERSONAS.EMAIL IS 'Correo electrónico';
COMMENT ON COLUMN SCCLINIC.PERSONAS.USER_CREATE IS 'Usuario que creó el registro';
COMMENT ON COLUMN SCCLINIC.PERSONAS.DATE_CREATE IS 'Fecha y hora de creación';
COMMENT ON COLUMN SCCLINIC.PERSONAS.USER_MODIFICATION IS 'Usuario que realizó la última modificación';
COMMENT ON COLUMN SCCLINIC.PERSONAS.DATE_MODIFICATION IS 'Fecha y hora de la última modificación';
COMMENT ON COLUMN SCCLINIC.PERSONAS.COMPANY_CODE IS 'Código de la compañía/empresa para multi-tenencia';

-- 2. ROLES
CREATE TABLE SCCLINIC.ROLES (
    ID UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    NOMBRE VARCHAR(50) NOT NULL UNIQUE,
    -- Audit fields
    USER_CREATE VARCHAR(100) NOT NULL,
    DATE_CREATE TIMESTAMP WITHOUT TIME ZONE DEFAULT CURRENT_TIMESTAMP NOT NULL,
    USER_MODIFICATION VARCHAR(100),
    DATE_MODIFICATION TIMESTAMP WITHOUT TIME ZONE,
    COMPANY_CODE VARCHAR(20) NOT NULL
);

COMMENT ON TABLE SCCLINIC.ROLES IS 'Define los roles de acceso para los usuarios';
COMMENT ON COLUMN SCCLINIC.ROLES.ID IS 'Identificador único del rol';
COMMENT ON COLUMN SCCLINIC.ROLES.NOMBRE IS 'Nombre del rol (ej: ROLE_ADMIN)';
COMMENT ON COLUMN SCCLINIC.ROLES.USER_CREATE IS 'Usuario que creó el registro';
COMMENT ON COLUMN SCCLINIC.ROLES.DATE_CREATE IS 'Fecha y hora de creación';
COMMENT ON COLUMN SCCLINIC.ROLES.USER_MODIFICATION IS 'Usuario que realizó la última modificación';
COMMENT ON COLUMN SCCLINIC.ROLES.DATE_MODIFICATION IS 'Fecha y hora de la última modificación';
COMMENT ON COLUMN SCCLINIC.ROLES.COMPANY_CODE IS 'Código de la compañía/empresa';

-- 3. USUARIOS
CREATE TABLE SCCLINIC.USUARIOS (
    ID UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    PERSONA_ID UUID UNIQUE,
    USERNAME VARCHAR(50) NOT NULL UNIQUE,
    PASSWORD VARCHAR(255) NOT NULL,
    ACTIVO BOOLEAN NOT NULL DEFAULT TRUE,
    -- Audit fields
    USER_CREATE VARCHAR(100) NOT NULL,
    DATE_CREATE TIMESTAMP WITHOUT TIME ZONE DEFAULT CURRENT_TIMESTAMP NOT NULL,
    USER_MODIFICATION VARCHAR(100),
    DATE_MODIFICATION TIMESTAMP WITHOUT TIME ZONE,
    COMPANY_CODE VARCHAR(20) NOT NULL,
    CONSTRAINT FK_USUARIO_PERSONA FOREIGN KEY (PERSONA_ID) REFERENCES SCCLINIC.PERSONAS(ID)
);

COMMENT ON TABLE SCCLINIC.USUARIOS IS 'Credenciales y estado de acceso de los usuarios';
COMMENT ON COLUMN SCCLINIC.USUARIOS.ID IS 'Identificador único del usuario';
COMMENT ON COLUMN SCCLINIC.USUARIOS.PERSONA_ID IS 'Referencia a la persona asociada';
COMMENT ON COLUMN SCCLINIC.USUARIOS.USERNAME IS 'Nombre de usuario para login';
COMMENT ON COLUMN SCCLINIC.USUARIOS.PASSWORD IS 'Contraseña encriptada';
COMMENT ON COLUMN SCCLINIC.USUARIOS.ACTIVO IS 'Estado de cuenta activo/inactivo';
COMMENT ON COLUMN SCCLINIC.USUARIOS.USER_CREATE IS 'Usuario que creó el registro';
COMMENT ON COLUMN SCCLINIC.USUARIOS.DATE_CREATE IS 'Fecha y hora de creación';
COMMENT ON COLUMN SCCLINIC.USUARIOS.USER_MODIFICATION IS 'Usuario que realizó la última modificación';
COMMENT ON COLUMN SCCLINIC.USUARIOS.DATE_MODIFICATION IS 'Fecha y hora de la última modificación';
COMMENT ON COLUMN SCCLINIC.USUARIOS.COMPANY_CODE IS 'Código de la compañía/empresa';

-- 4. USUARIO_ROLES (Join Table)
CREATE TABLE SCCLINIC.USUARIO_ROLES (
    USUARIO_ID UUID NOT NULL,
    ROLE_ID UUID NOT NULL,
    -- Audit fields
    USER_CREATE VARCHAR(100) NOT NULL,
    DATE_CREATE TIMESTAMP WITHOUT TIME ZONE DEFAULT CURRENT_TIMESTAMP NOT NULL,
    USER_MODIFICATION VARCHAR(100),
    DATE_MODIFICATION TIMESTAMP WITHOUT TIME ZONE,
    COMPANY_CODE VARCHAR(20) NOT NULL,
    PRIMARY KEY (USUARIO_ID, ROLE_ID),
    CONSTRAINT FK_UR_USUARIO FOREIGN KEY (USUARIO_ID) REFERENCES SCCLINIC.USUARIOS(ID),
    CONSTRAINT FK_UR_ROLE FOREIGN KEY (ROLE_ID) REFERENCES SCCLINIC.ROLES(ID)
);

COMMENT ON TABLE SCCLINIC.USUARIO_ROLES IS 'Relación muchos a muchos entre usuarios y roles';
COMMENT ON COLUMN SCCLINIC.USUARIO_ROLES.USUARIO_ID IS 'Referencia al usuario';
COMMENT ON COLUMN SCCLINIC.USUARIO_ROLES.ROLE_ID IS 'Referencia al rol';
COMMENT ON COLUMN SCCLINIC.USUARIO_ROLES.USER_CREATE IS 'Usuario que creó el registro';
COMMENT ON COLUMN SCCLINIC.USUARIO_ROLES.DATE_CREATE IS 'Fecha y hora de creación';
COMMENT ON COLUMN SCCLINIC.USUARIO_ROLES.USER_MODIFICATION IS 'Usuario que realizó la última modificación';
COMMENT ON COLUMN SCCLINIC.USUARIO_ROLES.DATE_MODIFICATION IS 'Fecha y hora de la última modificación';
COMMENT ON COLUMN SCCLINIC.USUARIO_ROLES.COMPANY_CODE IS 'Código de la compañía/empresa';

-- 5. PACIENTES
CREATE TABLE SCCLINIC.PACIENTES (
    ID UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    PERSONA_ID UUID NOT NULL UNIQUE,
    TIPO_SANGRE VARCHAR(5),
    SEGURO_MEDICO VARCHAR(100),
    CONTACTO_EMERGENCIA VARCHAR(255),
    OBSERVACIONES TEXT,
    -- Audit fields
    USER_CREATE VARCHAR(100) NOT NULL,
    DATE_CREATE TIMESTAMP WITHOUT TIME ZONE DEFAULT CURRENT_TIMESTAMP NOT NULL,
    USER_MODIFICATION VARCHAR(100),
    DATE_MODIFICATION TIMESTAMP WITHOUT TIME ZONE,
    COMPANY_CODE VARCHAR(20) NOT NULL,
    CONSTRAINT FK_PACIENTE_PERSONA FOREIGN KEY (PERSONA_ID) REFERENCES SCCLINIC.PERSONAS(ID)
);

COMMENT ON TABLE SCCLINIC.PACIENTES IS 'Información médica específica de los pacientes';
COMMENT ON COLUMN SCCLINIC.PACIENTES.ID IS 'Identificador único del paciente';
COMMENT ON COLUMN SCCLINIC.PACIENTES.PERSONA_ID IS 'Referencia a la persona asociada';
COMMENT ON COLUMN SCCLINIC.PACIENTES.TIPO_SANGRE IS 'Tipo de sangre del paciente';
COMMENT ON COLUMN SCCLINIC.PACIENTES.SEGURO_MEDICO IS 'Nombre del seguro médico';
COMMENT ON COLUMN SCCLINIC.PACIENTES.CONTACTO_EMERGENCIA IS 'Información de contacto para emergencias';
COMMENT ON COLUMN SCCLINIC.PACIENTES.OBSERVACIONES IS 'Notas u observaciones médicas generales';
COMMENT ON COLUMN SCCLINIC.PACIENTES.USER_CREATE IS 'Usuario que creó el registro';
COMMENT ON COLUMN SCCLINIC.PACIENTES.DATE_CREATE IS 'Fecha y hora de creación';
COMMENT ON COLUMN SCCLINIC.PACIENTES.USER_MODIFICATION IS 'Usuario que realizó la última modificación';
COMMENT ON COLUMN SCCLINIC.PACIENTES.DATE_MODIFICATION IS 'Fecha y hora de la última modificación';
COMMENT ON COLUMN SCCLINIC.PACIENTES.COMPANY_CODE IS 'Código de la compañía/empresa';

-- 6. HISTORIAS_CLINICAS
CREATE TABLE SCCLINIC.HISTORIAS_CLINICAS (
    ID UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    PACIENTE_ID UUID NOT NULL UNIQUE,
    -- Audit fields
    USER_CREATE VARCHAR(100) NOT NULL,
    DATE_CREATE TIMESTAMP WITHOUT TIME ZONE DEFAULT CURRENT_TIMESTAMP NOT NULL,
    USER_MODIFICATION VARCHAR(100),
    DATE_MODIFICATION TIMESTAMP WITHOUT TIME ZONE,
    COMPANY_CODE VARCHAR(20) NOT NULL,
    CONSTRAINT FK_HC_PACIENTE FOREIGN KEY (PACIENTE_ID) REFERENCES SCCLINIC.PACIENTES(ID)
);

COMMENT ON TABLE SCCLINIC.HISTORIAS_CLINICAS IS 'Registro de historias clínicas por paciente';
COMMENT ON COLUMN SCCLINIC.HISTORIAS_CLINICAS.ID IS 'Identificador único de la historia clínica';
COMMENT ON COLUMN SCCLINIC.HISTORIAS_CLINICAS.PACIENTE_ID IS 'Referencia al paciente asociado';
COMMENT ON COLUMN SCCLINIC.HISTORIAS_CLINICAS.USER_CREATE IS 'Usuario que creó el registro';
COMMENT ON COLUMN SCCLINIC.HISTORIAS_CLINICAS.DATE_CREATE IS 'Fecha y hora de creación';
COMMENT ON COLUMN SCCLINIC.HISTORIAS_CLINICAS.USER_MODIFICATION IS 'Usuario que realizó la última modificación';
COMMENT ON COLUMN SCCLINIC.HISTORIAS_CLINICAS.DATE_MODIFICATION IS 'Fecha y hora de la última modificación';
COMMENT ON COLUMN SCCLINIC.HISTORIAS_CLINICAS.COMPANY_CODE IS 'Código de la compañía/empresa';

-- 7. CITAS
CREATE TABLE SCCLINIC.CITAS (
    ID UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    PACIENTE_ID UUID NOT NULL,
    PROFESIONAL_ID UUID NOT NULL,
    FECHA_HORA TIMESTAMP WITHOUT TIME ZONE NOT NULL,
    MOTIVO VARCHAR(255) NOT NULL,
    ESTADO VARCHAR(20) DEFAULT 'PENDIENTE',
    -- Audit fields
    USER_CREATE VARCHAR(100) NOT NULL,
    DATE_CREATE TIMESTAMP WITHOUT TIME ZONE DEFAULT CURRENT_TIMESTAMP NOT NULL,
    USER_MODIFICATION VARCHAR(100),
    DATE_MODIFICATION TIMESTAMP WITHOUT TIME ZONE,
    COMPANY_CODE VARCHAR(20) NOT NULL,
    CONSTRAINT FK_CITA_PACIENTE FOREIGN KEY (PACIENTE_ID) REFERENCES SCCLINIC.PACIENTES(ID),
    CONSTRAINT FK_CITA_PROFESIONAL FOREIGN KEY (PROFESIONAL_ID) REFERENCES SCCLINIC.USUARIOS(ID)
);

COMMENT ON TABLE SCCLINIC.CITAS IS 'Agenda de citas médicas';
COMMENT ON COLUMN SCCLINIC.CITAS.ID IS 'Identificador único de la cita';
COMMENT ON COLUMN SCCLINIC.CITAS.PACIENTE_ID IS 'Referencia al paciente';
COMMENT ON COLUMN SCCLINIC.CITAS.PROFESIONAL_ID IS 'Referencia al profesional médico (usuario)';
COMMENT ON COLUMN SCCLINIC.CITAS.FECHA_HORA IS 'Fecha y hora programada de la cita';
COMMENT ON COLUMN SCCLINIC.CITAS.MOTIVO IS 'Motivo de la consulta';
COMMENT ON COLUMN SCCLINIC.CITAS.ESTADO IS 'Estado de la cita (PENDIENTE, COMPLETADA, CANCELADA)';
COMMENT ON COLUMN SCCLINIC.CITAS.USER_CREATE IS 'Usuario que creó el registro';
COMMENT ON COLUMN SCCLINIC.CITAS.DATE_CREATE IS 'Fecha y hora de creación';
COMMENT ON COLUMN SCCLINIC.CITAS.USER_MODIFICATION IS 'Usuario que realizó la última modificación';
COMMENT ON COLUMN SCCLINIC.CITAS.DATE_MODIFICATION IS 'Fecha y hora de la última modificación';
COMMENT ON COLUMN SCCLINIC.CITAS.COMPANY_CODE IS 'Código de la compañía/empresa';

-- 8. CONSULTAS
CREATE TABLE SCCLINIC.CONSULTAS (
    ID UUID PRIMARY KEY DEFAULT gen_random_uuid(),
    HISTORIA_ID UUID NOT NULL,
    CITA_ID UUID UNIQUE,
    DIAGNOSTICO TEXT,
    TRATAMIENTO TEXT,
    RECETA TEXT,
    FECHA TIMESTAMP WITHOUT TIME ZONE DEFAULT CURRENT_TIMESTAMP,
    -- Audit fields
    USER_CREATE VARCHAR(100) NOT NULL,
    DATE_CREATE TIMESTAMP WITHOUT TIME ZONE DEFAULT CURRENT_TIMESTAMP NOT NULL,
    USER_MODIFICATION VARCHAR(100),
    DATE_MODIFICATION TIMESTAMP WITHOUT TIME ZONE,
    COMPANY_CODE VARCHAR(20) NOT NULL,
    CONSTRAINT FK_CONSULTA_HISTORIA FOREIGN KEY (HISTORIA_ID) REFERENCES SCCLINIC.HISTORIAS_CLINICAS(ID),
    CONSTRAINT FK_CONSULTA_CITA FOREIGN KEY (CITA_ID) REFERENCES SCCLINIC.CITAS(ID)
);

COMMENT ON TABLE SCCLINIC.CONSULTAS IS 'Registro detallado de la consulta médica realizada';
COMMENT ON COLUMN SCCLINIC.CONSULTAS.ID IS 'Identificador único de la consulta';
COMMENT ON COLUMN SCCLINIC.CONSULTAS.HISTORIA_ID IS 'Referencia a la historia clínica';
COMMENT ON COLUMN SCCLINIC.CONSULTAS.CITA_ID IS 'Referencia a la cita asociada';
COMMENT ON COLUMN SCCLINIC.CONSULTAS.DIAGNOSTICO IS 'Detalles del diagnóstico médico';
COMMENT ON COLUMN SCCLINIC.CONSULTAS.TRATAMIENTO IS 'Descripción del tratamiento indicado';
COMMENT ON COLUMN SCCLINIC.CONSULTAS.RECETA IS 'Medicamentos o indicaciones recetadas';
COMMENT ON COLUMN SCCLINIC.CONSULTAS.FECHA IS 'Fecha de la consulta';
COMMENT ON COLUMN SCCLINIC.CONSULTAS.USER_CREATE IS 'Usuario que creó el registro';
COMMENT ON COLUMN SCCLINIC.CONSULTAS.DATE_CREATE IS 'Fecha y hora de creación';
COMMENT ON COLUMN SCCLINIC.CONSULTAS.USER_MODIFICATION IS 'Usuario que realizó la última modificación';
COMMENT ON COLUMN SCCLINIC.CONSULTAS.DATE_MODIFICATION IS 'Fecha y hora de la última modificación';
COMMENT ON COLUMN SCCLINIC.CONSULTAS.COMPANY_CODE IS 'Código de la compañía/empresa';

